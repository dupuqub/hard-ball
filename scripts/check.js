G.check =
{
  //....................................................................................................................
  //
  pool :
  [
    // 'pooled' event example
    {
      clear : true ,
      test  : _ => true ,
      act   : _ => true
    } ,
  ] ,

  //....................................................................................................................
  //
  tool : _ =>
  {
    G.check.pool.forEach( ( item_0 , $_0 ) =>
    {
      if( item_0.test() )
      {
        const safe_act = item_0.act

        if( item_0.clear )
        {
          G.check.pool = G.check.pool.filter( ( item_1 , $_1 ) => $_0 !== $_1 )
        }

        safe_act()
      }
    } )
  }
}


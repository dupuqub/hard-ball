
`use strict`

//......................................................................................................................

G.check =
{
  //....................................................................................................................

  pool:
  [
    // "pooled" event example
    {
      clear: true,
      test: () => true,
      act: () => true,
    },
  ],

  //....................................................................................................................

  tool: () =>
  {
    G.check.pool.forEach((item0, index0) =>
    {
      if(item0.test())
      {
        const safeAct = item0.act

        if(item0.clear)
        {
          G.check.pool = G.check.pool.filter((unused, index1) => index0 !== index1)
        }

        safeAct()
      }
    })
  }
}

